      Niin kauan kuin Lassi jaksoi muistaa, hänen syntymäpäivänsä oli
      alkanut samalla tavalla. Aikaisin aamulla, kun isä ja äiti vielä luulivat
      hänen nukkuvan, he hiipivät hiljaa keittiöön ja katsoivat
      jännittyneinä jotain, joka mahtui isän kämmenelle piiloon. Vähän
      ajan päästä he huokaisivat helpotuksesta, äiti sanoi että kylläpä häntä
      jännitti ja isä sanoi että no niin, taas helpotti vuodeksi. Sitten he
      halasivat, Lassi hiipi keittiön oven takaa takaisin nukkumaan ja
      mietti, milloin hän ymmärtäisi mistä ihmeessä siinä oli kysymys.
      Vuosi toisensa jälkeen sama kuvio toistui.

      Kunnes tuli sellainen Lassin syntymäpäivän aamu, jolloin isä
      ja äiti eivät huokaisseetkaan helpotuksesta.
      
      ”Nyt se sitten alkaa”, äiti sanoi hiljaa.
      
      ”Nyt se sitten alkaa”, isä vastasi karhealla äänellä.

      * * *
      
      Koko päivän Lassi piti isää ja äitiä silmällä nähdäkseen, mikä alkaisi,
      mutta turhaan. Iltapäivään mennessä syntymäpäivä oli edennyt varsin
      tutulla tavalla. Isä oli hukannut silmälasinsa kolmesti ennen
      puoltapäivää ja etsiessään haronut tukkansa samanlaiseksi
      hiekanväriseksi pörröksi kuin Lassillakin. Äiti oli varustellut
      kahvipöytää kuin olisi odottanut kokonaista armeijaa vierailulle, ja
      sukulaisvieraille oli kerrottu samat nolostuttavat vauvatarinat kuin
      aiempinakin vuosina. Ehkä ”se”, mikä alkaisi, oli myöhässä?
      
      Mutta sitten viimeisetkin sukulaisvieraat lähtivät, ja isä
      ilmoitti, että heillä äidin kanssa oli Lassille asiaa.
      
      Oli Lassi kyllä jo odottanutkin. Paitsi sitä salaperäistä
      ”jotakin” joka alkaisi, myös isän ja äidin hankkimaa lahjaa, jonka
      nämä antoivat aina vasta vieraitten lähdettyä. Hänellä oli
      aavistuksensa lahjasta, mutta myös aavistuksen ohittavia toiveita.
      Ehkä isä ja äiti olivat heltyneet ja ostaneet hänelle jotain mitä hän
      todella halusi, sen sijaan mitä hän tarvitsi? Oliko se nyt hänen
      vikansa, etteivät avaimet pysyneet hänellä tallessa, ja että siitä syystä
      pyörä tai pari oli aikojen kuluessa, tai vaikka ihan vastakin,
      varastettu? Pitikö siitä nyt syntymäpäivänä rangaista?
      
      Isän ilme kertoi hänen olevan kohtuullisen tyytyväinen
      siihen, mitä he aikoivat äidin kanssa kohta kertoa. Hän kohensi
      silmälasejaan ja ryhtiään ja näytti hetken verran ihan innokkaalta
      pikkupojalta. Lassi tunsi toivon viriävän sisällään.

      ”Tulehan tänne”, isä viittasi.

      Äiti seisoi jo hymyillen työhuoneen ovella. Nyt kun vieraat
      olivat lähteneet, hänkin näytti taas rennommalta. Ruskeat suortuvat
      karkailivat sykeröstä jolle ne oli vieraiden ajaksi pakotettu, eikä hän
      enää yrittänyt sukia niitä paikoilleen. Lassi tullessa lähemmäksi hän
      tempaisi Lassin tiukkaan halaukseen: ”Meidän poika!” Lassi oli jo
      melkein äidin mittainen, ja saattoi helposti kiemurrella otteesta irti.
      Heti kun halusi.
      
      Äidin äänensävy oli lämmin. Se tuntui lupaavan Lassille, että
      armo saisi käydä (taas) oikeudesta, ja hän saisi sittenkin jotain
      oikeasti toivomaansa. Olisiko lahja oma tietokone? Tai sittenkin
      videokamera? Tai ehkä viikkojen maanittelu oli tuottanut tulosta, ja
      hän pääsisi sukellusleirille. Tai ehkä...
      
      ”Tässä olisi”, isä keskeytti Lassin innokkaat mietteet.
      
      Keskellä isän työhuonetta seisoi tuliterä polkupyörä.
      
      Siis sittenkin hyötylahja. Lassi yritti pitää ilmeensä kurissa ja
      lähestyi pyörää. No, olihan uudessa pyörässä silti aina oma hohtonsa,
      varsinkin jos sitä vertasi täysin pyörättömään olotilaan.
      
      Pyörässä oli 12 vaihdetta, leveät maastorenkaat,
      teräksenvärinen ohjaustanko ja tummanvihreä runko. Pari viikkoa
      aiemmin isä oli näyttänyt hänelle kuvastoa ja kysynyt, mikä pyörä
      olisi eniten hänen mieleensä. Jos isää siis sattuisi huvittamaan ottaa
      sellainenkin asia huomioon. Koska hän oli näyttänyt juuri tuon
      näköistä pyörää, täytyi päätellä että isää oli huvittanut. Paitsi yhdessä
      kohtaa.
      
      ”Eikö niitä ollut mustina?”

      Isä ja äiti vilkaisivat toisiinsa. Isä huokaisi. Lassi kiirehti
      sanomaan, ettei vihreässä mitään vikaa ollut, hän oli vain maininnut
      että musta olisi hieno ja siksi hän oli luullut että... mutta että tietysti
      tummanvihreällä ajoi ihan samalla lailla kuin mustallakin, ei siinä
      mitään, ja pyörä oli hieno, kiitoksia paljon.
      
      ”Meillä olisi sinulle vielä yksi yllätys”, äiti sanoi
      varovaisesti.
      
      Lassi yllättyi jo tästä. Heillä oli tapana, että kukin sai
      syntymäpäivänään joko yhden ison tai pari kolme pienempää lahjaa,
      ja hän oli ymmärtänyt että jos pyörä hankittaisiin, muuta ei
      kannattaisi odottaa.
      
      ”Mikä yllätys se on?”
      
      ”Katsohan tätä”, isä sanoi.
      
      Lassi vilkaisi isän osoittamaan suuntaan. Seinällä oli
      kehystetty diplomi, jonka hän oli saanut jossain koulun
      laskukilpailussa alaluokilla, aikoja aikoja sitten. Se oli seinällä isän
      ja ukin kirjanpitäjädiplomin sekä ukin isän vastaavan, noin Nooan
      arkin aikaisen todistuksen kanssa. Ne kaikki olivat olleet työhuoneen
      seinällä niin kauan kuin Lassi muisti.
      
      ”Niin?”
      
      Isä ja äiti vilkaisivat toisiinsa. Isä suoristi selkäänsä.
      
      ”Sinähän tiedät että meillä on ollut perheessä tämä tilitoimisto jo
      kauan. Sen perusti sinun isoisoisäsi, ja sinun ukkisi meni sinne töihin
      heti koulunsa jälkeen, ja minäkin suoraan kauppakoulusta.”
      Kyllähän Lassi tiesi. Korkometsän Tilitoimisto oli perheen
      ylpeys. ”Tilitoimisto” oli itse asiassa ensimmäinen sana jonka hän
      muisti osanneensa lukea, oman nimensä jälkeen tietysti. Ja hän
      muisti miten ylpeä isä oli ollut kuultuaan asiasta. Mutta tämä kaikki
      oli tuttuakin tutumpaa perhehistoriaa, ei sitä olisi tarvinnut erikseen
      kerrata.
      
      ”Entä sitten?”
      
      ”No, sinähän tiedät että... että...” Isä näytti epäröivän hetken
      verran, mutta veti sitten henkeä ja lausui kuin toistaisi ulkoa opittua
      läksyä: ”Me olemme aina ajatelleet että jonain päivänä sinä kenties
      jatkat tätä yritystä. Ja koska olet selvästi perinyt sukusi
      matemaattisen lahjakkuuden”, isä viittasi laskukilpailun diplomiin,
      ”olemme ajatelleet että on jo aika astua askel eteenpäin. Me olemme
      ilmoittaneet sinut viikon matematiikkaleirille! Se alkaa reilun viikon
      päästä niin että ehdit vähän levähtääkin nyt koulun jälkeen, leiriä
      pidetään aamukymmenestä...”
      
      Lassia kylmäsi. Loput isän puheesta menivät hänen ohitseen,
      ja hän vain tuijotti isää ja nyökyttelevää äitiä vuoronperään. Siis
      mitä? Kuka hänen iässään miettisi perheyrityksessä jatkamista, tai
      ainakaan tilitoimistossa! Ehkä, jos perheessä piirrettäisiin sarjakuvia
      tai kuvattaisiin seikkailufilmejä, Lassi myönsi itselleen, mutta
      tilitoimisto – eihän hän edes kunnolla tiennyt mitä siellä tehtiin!
      Numeroita, ja laskuja, ja ja mitä ties todistuksia, ja loputonta
      tietokoneen kanssa värkkäämistä. Ja oli pikkuisen kohtuutonta, että
      jos oli voittanut päässälaskukilpailun joskus lähes edesvastuuttoman
      nuorena – että jos 7- tai 8-vuotiaana osasi laskea että 11+24 on 36,
      hmm, siis 35, että siitä rangaistaisiin vuosia myöhemmin?

      Kaiken tämän Lassi huusi, mutta vain mielessään. Hänen
      suunsa ei kyennyt muodostamaan yhtä ainutta sanaa, niin järkyttynyt
      hän oli. Isä jatkoi edelleen, jotain mitä kaikkea kivaa leirillä
      käsiteltäisiin, ja kuinka hyvällä ilmalla ohjelmaa järjestettäisiin
      ulkosallakin. Silloin Lassi sai viimein suunsa tottelemaan ja rääkäisi,
      että ei varmasti.
      
      Isä keskeytti lauseensa ja näytti hieman hämmentyneeltä.

      ”Etkö sinä halua että ohjelmaa on ulkonakin?”

      ”Ei, kun minähän en sinne mene”, hän kiljahti. ”Mitä te
      oikein ajattelette, kesälomalla matikkaleirille! Oletteko te järjiltänne?
      Ja sitä paitsi kuka sanoo että minä jatkan mitään yritystä kun olen
      iso? Sinne on vielä vuosia!”

      Isä ja äiti vaihtoivat katseen. Äiti liikahti ja aloitti: ”Katsos
      asia on niin - ” kun isä keskeytti ja yritti mumista niin hiljaa ettei
      Lassi kuulisi: ”Ehkä siitä ei kannata puhua vielä.”

      ”Mitä? Mistä minulle ei vielä kannata puhua? Kertokaa NYT!”

      Lassia aivan hengästytti. Hän oli varma ettei ollut käyttänyt
      niin montaa huutomerkkiä puheessa sitten uhmaiän, mutta nyt olikin
      tosi kyseessä.

      Olihan hän kuullut vihjailuja perheyrityksessä jatkamisesta
      ennenkin. Ukki oli monet kerrat laskenut kätensä Lassin olkapäälle ja
      aloittanut, että ”sitten kun toimisto olisi Lassin”, mutta ei hän ollut
      sitä tosissaan ottanut. Kuka ottaisi? Kun työnteko ja aikuisuus odotti
      jossain kaukaisessa horisontissa, ja maailmassa oli niin monta
      mielenkiintoista asiaa tutkittavana?

      Äiti ja isä näyttivät käyvän sanatonta neuvottelua hetken, ja
      sitten äiti istahti tuolille Lassin eteen. Hän näpräsi vihkisormustaan,
      kuten aina teki kun halusi sanoa jotain vakavaa, ja aloitti sitten
      hieman haparoiden: ”Asia on nyt niin että – tätä sinun voi olla vaikea
      uskoa mutta – ” hän keskeytti ja vilkaisi isää kuin vahvistusta
      saadakseen. Isä nyökkäsi, ja äiti jatkoi: ”Laskeminen ja matematiikka
      ovat ikään kuin osa tämän perheen tehtävää tässä maailmassa. Ei se
      ehkä sinua vielä innosta mutta - ”

      Lassi ponkaisi pystyyn. ”No ei totta vieköön innosta! Siihen
      on vuosia ennen kuin minä edes voin mennä kunnolla töihin!”

      Äiti tarttui lempeästi poikaansa kädestä, ja veti hänet
      istumaan. ”Minäpä aloitan uudestaan. Juttu on niin, että sinun täytyy.
      Ei, älä sano vielä mitään”, hän kielsi nähdessään Lassin taas
      kimpaantuneena avaavan suunsa, ”sillä sellainen kirous on meidän
      päällemme asetettu. Ellet sinä mene nyt sille matematiikkaleirille, tai
      sitten muuten vain laske kesäkuun kolmatta viikkoa, niin koko
      meidän perheemme ja sukumme aina alkuesi-isiin saakka muuttuu
      tomuksi ja tuhkaksi ja haihtuu maailmaan tuuliin.”

      * * *
      
      Tietenkään Lassi ei uskonut sitä. Sehän kuulosti juuri sellaiselta
      älyttömältä uhkaukselta jollaisia äiti saattoi keksiä saadakseen heidät
      tekemään kotitöitä: ”Ellet sinä nyt tyhjennä tiskikonetta, saat
      kymmenen vuoden vessanpesuvuoron, ja saat nukkuakin vessaharjan
      vieressä.” ”Jos et välittömästi siivoa huonettasi, minä siirrän sinut
      asumaan pyykkikoriin, kun likaiset vaatteet kerran ovat sinulle niin
      rakkaita.” Hah hah, hyvä juttu. Paha vain, että tällä kertaa äiti ei
      keksinytkään sitä itse.
      
      Kävi ilmi, että ukin ukin ukin isä tai joku muu tosi kaukainen
      tyyppi oli aikoinaan ollut aivan onneton laskemaan. Ja sitten yhtenä
      tämän pojan syntymäpäivänä hänen isänsä oli lähettänyt pojan
      ostamaan leipälapiota, ja tämä oli saanut väärin rahasta takaisin.
      Tästä hänen isänsä oli suuttunut aivan tolkuttomasti ja lähettänyt
      poikansa arestiin maakellariin. Takaisin poika saisi tulla vasta kun
      olisi oppinut kunnolla laskemaan ja osaisi sanoa tarkalleen, kuinka
      monta perunaa laareissa oli sekä miten monta ateriaa niistä saisi
      tehtyä. Poika oli itkenyt ja laskenut ja laskenut ja itkenyt, mutta kun
      hän viimein tuli arestista pois, hän tiesi perunat pilkulleen ja koon
      mukaan lajiteltuina ja osasi kertoa täsmälleen, kuinka monta kertaa
      perhe saisi niistä syödäkseen. Isän ihmetellessä laskutaidon äkillistä
      ilmestymistä poika oli kertonut miten ystävällinen pieni maahinen oli
      ilmestynyt kellariin auttamaan häntä.
      
      Apu ei tosin tullut aivan korvauksetta. Apua saadakseen
      pojan oli luvattava että hän, ja sitten joskus tulevaisuudessa hänen
      vanhin poikansa, ja tämän vanhin poika, ja niin edelleen aina hamaan
      maailman loppuun saakka, viettäisivät korvaukseksi joka kesäkuun
      kolmannen viikon laskemalla. Ja että hän ja kaikki nuo tulevat pojat
      tekisivät aikuisina työtä numeroiden parissa. Ja että sen lisäksi he
      eivät missään tapauksessa, missään olosuhteissa käyttäisi mustia
      vaatteita. Jos joku ei tottelisi näitä ehtoja, hän ja koko suku aina
      esihistoriaan saakka pyyhkiytyisi pois, ei vain tästä vaan kaikista
      muistakin maailmoista.
      
      ”Mitä, ei mustia vaatteita”, Lassi huudahti tarttuen siihen
      ainoaan kohtaan tarinaa jonka hänen aivonsa ymmärsivät. ”Tuohan
      on naurettavaa! Onhan isälläkin...” Sitten hän vaikeni. Vaikka hän
      kuinka yritti muistella, ei hän saanut päähänsä yhden yhtä mustaa
      vaatekappaletta isänsä vaatekomerosta. Mutta kieltää nyt nuorta
      miestä käyttämästä mustaa! Älytöntä! Kas kun ei saman tien
      määrätty käyttämään alushousuja pipon sijaan. ”Mistä te voitte tietää
      että tuo on totta”, hän tivasi. ”Jos se oli juoni jolla se tosi-isoisä
      halusi saada poikansa laskemaan? Väitättekö te, että kaikki ovat siitä
      lähtien muka laskeneet viikon vuodessa, vain koska joku vanha satu
      niin käskee?”
      
      ”Muistelepa vaikka viime kesää”, äiti sanoi lempeästi.
      
      Lassi aikoi juuri väittää, ettei silloin kukaan ainakaan ollut
      laskenut mitään, kun hän muisti: juhannuksenalusviikko. Hän oli
      kärttänyt vanhempiaan lähtemään mökille, mutta isä oli sanonut että
      hänellä oli liikaa töitä. Tilitoimistossa. Jossa hän siis teki töitä
      numeroiden kanssa. Ja minne ukki, isän isä siis, oli tullut
      eläkepäiviltään auttamaan, juuri siksi viikoksi vain, niin ettei hän
      ollut voinut mennä ukinkaan kanssa kalaan.
      
      Jopa juhannuksena he olivat laskeneet – huvikseen, muka –
      montako naulaa laituriin oli lyöty ja miten monta tanssiaskelta oli
      jossain tangossa. Lassi oli pitänyt sitä vain ikävänä ammattitautina,
      eikä ollut kiinnittänyt asiaan sen kummemmin huomiota. Sehän oli
      heillä oikein perhevitsikin: ”Ei meidän perheen miehet juhannusta
      laskematta pärjää!” Ja sitten oli ehdoteltu mitä 
      mielikuvituksellisempia aiheita laskuille.
      
      Mutta isä oli tehnyt sitä aina, ei vain yhtenä viikkona
      vuodessa. Hän saattoi ykskaks osoittaa vaikka kirjahyllyä ja
      huomauttaa: ”Tiesittekös että tuossa hyllyssä on 37 punakantista
      kirjaa?”
      
      Ukki teki sitä samaa, Lassi tajusi. Hänenkin oli kai pakko.
      Lassi oli vain ajatellut että isä oli oppinut sen ukilta. Joskus he oikein
      haastoivat toisiaan: ”Et kyllä pysty laskemaan puolessa minuutissa
      montako harmaata raitaa tuossa tapetissa on ovesta ikkunaan asti.” Ja
      sitten toinen otti aikaa, ja toinen laski.
      
      ”Meillä on tämä”, isä sanoi hiljaa herättäen Lassin
      ajatuksistaan, ja antoi hänelle taskukellon.
      
      Se oli ukin isälle valmistujaislahjaksi antama perintökello
      (tai niin Lassi oli ajatellut), ja hän oli nähnyt sen lukemattomia
      kertoja. Siinä ei ollut mitään jännittävää eikä erikoista, paitsi ehkä
      kellon selkämyksessä keskellä oleva reikä. Pienenä hän oli luullut,
      että sitä kautta aika kaadettiin kelloon sisälle.
      
      ”Tästä kellosta me sen näimme. Että sinun on... alettava elää
      kirouksen ehtojen mukaan.”
      
      Tätäkö isä ja äiti olivat katsoneet aina hänen
      syntymäpäivänsä aamuna? Vanhaa kelloa?
      
      ”Näitte kellosta? Että jaa, kello on puoli yksitoista, aika
      alkaa kiroukselle vai?”
      
      ”Katso itse.” Ja Lassin katsoessa isä painoi kellon nuppia, ja
      varsinainen kellotaulu kiertyi sivuun. Sen alla oli toinen pyöreä
      taulu, jonka yläreunaan oli kaiverrettu kiemuraisella kirjoituksella:
      Ettet unohuttaisi... Itse taulussa oli numeroiden sijaan Lassin kuva.
      Joka liikkui.
      
      Kuvan taustalla näkyi kalenteri, josta saattoi tarkistaa
      päiväyksen: 16.6. Siis kesäkuun kolmas viikko. Etualalla pompahteli
      innokkaana Lassi itse. Hän oli näköjään juuri vetämässä mustaa
      paitaa päälleen, ja potkaisi samalla nautinnollisesti lattialla lojuvaa
      paperipinoa, jonka päällimmäisissä papereissa vilahti tiheään
      printattuja laskuja. Sen jälkeen kuvan poika hävisi kuvasta hitaasti ja
      kaikesta päätellen vähemmän nautinnollisesti. Tämän jälkeen kuvaan
      ilmestyivät Lassin vanhemmat ja sisko, jotka hekin häipyivät
      hiljalleen olemattomiin, samoin kuin isovanhemmat, heidän
      vanhempansa ja aika moni muu joita Lassi ei äkkiseltään tunnistanut,
      mutta joille häviäminen tuli näköjään hienoisena ja epämiellyttävänä
      yllätyksenä.
      Lopulta kuvassa näkyi enää sumuinen ja epämiellyttävän tyhjä maisema.
      
      ”Kello on nyt sinun”, isä sanoi. ”Niin että miten olisi sen
      matematiikkaleirin kanssa”, hän kysyi vaimeasti.
      
      Lassi nyökäytti päätään hitaasti.
      
      * * *
      
      Mikä on sopiva korvaus kirouksesta, joka varjostaa koko
      loppuelämää?
            
      Olisiko miljoona mitään? Tai
      maailmanympärysmatka? Unelmaloma Havaijilla, vapautus koulusta,
      rajoittamaton oikeus katsoa niin paljon elokuvia kuin ikinä jaksaa?
      Lassista tuntui, että sen kuitenkin pitäisi olla jotain enemmän
      kuin yksi vaivainen polkupyörä. Oli pyörä miten hieno tahansa, ja oli
      kirous miten... toteutettava tahansa.
      
      Enkä minä ole edes seitsemännen pojan seitsemäs poika, hän
      mietti synkkänä huoneessaan hetkeä myöhemmin. Lassi oli lukenut
      tarinan jos toisenkin, ja tiesi miten asioiden kuuluisi mennä.
      Tarinoiden kirouksissa oli edes jotain järkeä. Sankareiden niskaan ei
      vain kaadettu vanhaa kirousta kuin likaista pesuvettä, vaan he olivat
      tehneet jotain sen ansaitakseen. Kieltäytyneet antamasta vettä
      janoiselle vanhalle naiselle, joka olikin valepukuinen haltija, tai
      sitten jonkun henki oli vaarassa ellei sopimusta syntyisi. He siis
      suostuivat kauppaan vasta äärimmäisessä hätätilanteessa, mutta mitä
      tekee hänen esi-isänsä? Joutuu perunakellariin pariksi tunniksi –
      tuskin äärimmäinen hätätila - ja suostuu heti surkeimpaan
      sopimukseen mitä ensimmäinen paikalle osunut pikku-ukko
      ehdottaa. Mikä yleensä edes oli pahinta mitä perunakellarissa olisi
      saattanut tapahtua? Käsien likaantuminen? Perunaan liukastuminen?
      Ei mitään itsehillintää eikä arvokkuutta sillä pojalla.
      ”Epäreilua”, Lassi puuskahti. ”Aivan hirveän, kamalan,
      uskomattoman epäreilua!”
      
      Saduissa sentään oli joku keino purkaa kirous, suudeltiin
    jotain tai kudottiin nokkospaitaa vuositolkulla puhumatta. Mutta ketä
    hän suutelisi? Sitä maahista? Kelloa? Itseään? Nokkosista voisi
    selvitäkin, mutta puhumattomana oleminen vasta rankkaa olisi. Yhtä
    kaikki, silloin olisi ollut edes jotain toivoa. Nyt kaikki oli yhtä
    mustaa. Paitsi ettei voinut olla, oikeasti.
      
      ”Isä unohti tämän”, kuului äidin ääni ovelta. Lassi kääntyi
      katsomaan.
      
      Äidin kämmenellä lepäsi kymmenisen senttiä pitkä hopeinen
      avain. Se oli koristeltu säkenöivin sinisin kivin, ja kesävalon
      täyttämässä huoneessakin se näytti hehkuvan ihan omaa, kirkasta
      hohdettaan.
      
      Ehkä kirous oli sittenkin mahdollista kumota, juuri tuolla
      avaimella?
